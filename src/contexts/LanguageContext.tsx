const langCodes = ["en", "hi", "bn", "pa", "mr", "ta", "te", "gu", "kn", "ml"];

// Build locales from compact format
const locales: Record<string, Record<string, string>> = {};
langCodes.forEach((code, i) => {
  locales[code] = {};
  Object.entries(commonKeys).forEach(([key, values]) => {
    locales[code][key] = values[i] || values[0];
  });
});

const LANGUAGE_NAMES: Record<string, string> = {
  en: "English", hi: "हिन्दी", bn: "বাংলা", pa: "ਪੰਜਾਬੀ",
  mr: "मराठी", ta: "தமிழ்", te: "తెలుగు", gu: "ગુજરાતી",
  kn: "ಕನ್ನಡ", ml: "മലയാളം",
};

interface LanguageContextType {
  lang: string;
  setLang: (l: string) => void;
  t: (key: string) => string;
  languages: typeof LANGUAGE_NAMES;
}

const LanguageContext = createContext<LanguageContextType | null>(null);

export const useLanguage = () => {
  const ctx = useContext(LanguageContext);
  if (!ctx) throw new Error("useLanguage must be used within LanguageProvider");
  return ctx;
};

export const LanguageProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLangState] = useState(() => localStorage.getItem("lang") || "en");

  const setLang = (l: string) => {
    setLangState(l);
    localStorage.setItem("lang", l);
  };

  const t = (key: string) => locales[lang]?.[key] || locales.en[key] || key;

  return (
    <LanguageContext.Provider value={{ lang, setLang, t, languages: LANGUAGE_NAMES }}>
      {children}
    </LanguageContext.Provider>
  );
};
